hpc <- read.table("household_power_consumption.txt", header =  TRUE, sep = ";", na.strings = "?")
hpc$DateTime <- paste(hpc$Date, hpc$Time, sep = " ")
hpc$DateTime <- strptime(hpc$DateTime, format = "%d/%m/%Y %H:%M:%S", tz = "")
hpc$Date <- as.Date(hpc$Date, format = "%d/%m/%Y")
hpc <- subset(hpc, hpc$Date >= "2007-02-01" & hpc$Date <= "2007-02-02")
hpc$Time <- strptime(hpc$Time, format = "%H:%M:%S", tz = "")
hpc <- transform(hpc, Day = weekdays(hpc$Date))
plot(hpc$DateTime, hpc$Global_active_power, type = "n", xlab = "", ylab = "Global Active Power (kilowatts)")
lines(hpc$DateTime, hpc$Global_active_power, type = "l")
